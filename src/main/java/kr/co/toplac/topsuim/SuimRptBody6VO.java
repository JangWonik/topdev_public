package kr.co.toplac.topsuim;

public class SuimRptBody6VO {
	
	private String serialNo;
	private String suimRptNo;
	private String requestDtl;
	private String daysInpatient;
	private String daysOutpatient;
	public String getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}
	public String getSuimRptNo() {
		return suimRptNo;
	}
	public void setSuimRptNo(String suimRptNo) {
		this.suimRptNo = suimRptNo;
	}
	public String getRequestDtl() {
		return requestDtl;
	}
	public void setRequestDtl(String requestDtl) {
		this.requestDtl = requestDtl;
	}
	public String getDaysInpatient() {
		return daysInpatient;
	}
	public void setDaysInpatient(String daysInpatient) {
		this.daysInpatient = daysInpatient;
	}
	public String getDaysOutpatient() {
		return daysOutpatient;
	}
	public void setDaysOutpatient(String daysOutpatient) {
		this.daysOutpatient = daysOutpatient;
	}
	
	
}
