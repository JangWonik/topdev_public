package kr.co.toplac.topsuim;

public class SuimRptSagoBscVO {
	
	private String sagoNo;
	private String item;
	private String content ;
	private String regDate ;
	private String suimRptNo ;
	
	public String getSagoNo() {
		return sagoNo;
	}
	public void setSagoNo(String sagoNo) {
		this.sagoNo = sagoNo;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getSuimRptNo() {
		return suimRptNo;
	}
	public void setSuimRptNo(String suimRptNo) {
		this.suimRptNo = suimRptNo;
	}
}
