package kr.co.toplac.sysadm;

public class SysAdmHolidayVO {
	
	private String holiday_date;
	private String holiday_yoil;
	private String holiday_code;
	private String holiday_code_nm;
	private String holiday_basis;

	public String getHoliday_date() {
		return holiday_date;
	}
	public void setHoliday_date(String holiday_date) {
		this.holiday_date = holiday_date;
	}
	public String getHoliday_yoil() {
		return holiday_yoil;
	}
	public void setHoliday_yoil(String holiday_yoil) {
		this.holiday_yoil = holiday_yoil;
	}
	public String getHoliday_code() {
		return holiday_code;
	}
	public void setHoliday_code(String holiday_code) {
		this.holiday_code = holiday_code;
	}
	public String getHoliday_code_nm() {
		return holiday_code_nm;
	}
	public void setHoliday_code_nm(String holiday_code_nm) {
		this.holiday_code_nm = holiday_code_nm;
	}
	public String getHoliday_basis() {
		return holiday_basis;
	}
	public void setHoliday_basis(String holiday_basis) {
		this.holiday_basis = holiday_basis;
	}

}
